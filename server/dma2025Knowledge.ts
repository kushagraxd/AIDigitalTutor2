export const addDMA2025Knowledge = async (storage: any) => {
  console.log("Adding DMA 2025 course knowledge entries...");
  let validEntries = 0;
  let totalEntries = dma2025KnowledgeData.length;
  
  for (const entry of dma2025KnowledgeData) {
    try {
      // Check if the module exists
      const module = await storage.getModuleById(entry.moduleId);
      
      if (module) {
        await storage.createKnowledgeBaseEntry({
          title: entry.title,
          content: entry.content,
          category: entry.category,
          moduleId: entry.moduleId,
          source: "DMA 2025 Course",
          embedding: [] // This will be populated by the OpenAI service
        });
        validEntries++;
      }
    } catch (error) {
      console.error(`Error adding knowledge entry "${entry.title}":`, error);
    }
  }
  
  console.log(`Successfully added ${validEntries} DMA 2025 course knowledge entries (out of ${totalEntries} total entries).`);
};